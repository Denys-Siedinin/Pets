package Animals;

public class Dog {
  public String name;
  public float strange, age, weitgh;

  public float getStrange() {
    return strange;
  }

  public void setStrange(float strange) {
    this.strange = strange;
  }

  public float getAge() {
    return age;
  }

  public void setAge(float age) {
    this.age = age;
  }

  public float getWeitgh() {
    return weitgh;
  }

  public void setWeitgh(float weitgh) {
    this.weitgh = weitgh;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


}